    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->input();
        $validation = Validator::make($data,[
            'fullname'=>'string|required|max:20',
            'email'=>'email|required|max:20',
            'password'=>'regex:/^(?=.*[0-9]+.*)(?=.*[a-zA-Z]+.*)[0-9a-zA-Z]{6,}$/|max:20'
        ]);

        if($validation->fails() == false){
        $user = new User();
        $user->name = trim($data['fullname']);
        $user->email = trim($data['email']);
        $user->password = Hash::make(trim($data['password']));
        $user->save();

        return redirect('/')->with('success','Sign up Successful');
        }
        else{
        return redirect('/')->with('error','Sign up Failed');
        }

    }

